# helper function
def showTesthand(test):
    hand = []
    for s in test[0]:
        for p in s:
            hand.append(p)
    ophand = []
    for s in test[1]:
        for p in s:
            ophand.append(p)
    return Pai.showHand(hand) + Pai.showHand(ophand)

